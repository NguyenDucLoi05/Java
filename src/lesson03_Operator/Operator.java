package lesson03_Operator;

public class Operator {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a = " +a);
        System.out.println("b = " +b);
        System.out.println("a + b = " +(a+b));
        System.out.println("a - b = " +(a-b));
        System.out.println("a * b = " +(a*b));
        System.out.println("a / b = " +((double)a/b));
        System.out.println("a % b = " +(a%b));
    }
}
